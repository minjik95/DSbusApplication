package ds.ac.kr.dsbusapplication;

public class ArrivalInfo {

    private String arsId;
    private String firstTm;
    private String lastTm;
    private String stNm;
    private int sectOrd1;

    public String getArsId() {
        return arsId;
    }

    public void setArsId(String arsId) {
        this.arsId = arsId;
    }

    public String getFirstTm() {
        return firstTm;
    }

    public void setFirstTm(String firstTm) {
        this.firstTm = firstTm;
    }

    public String getLastTm() {
        return lastTm;
    }

    public void setLastTm(String lastTm) {
        this.lastTm = lastTm;
    }

    public String getStNm() {
        return stNm;
    }

    public void setStNm(String stNm) {
        this.stNm = stNm;
    }

    public int getSectOrd1() { return sectOrd1; }

    public void setSectOrd1(int sectOrd1) { this.sectOrd1 = sectOrd1; }



}
