package ds.ac.kr.dsbusapplication;

public class PositionInfo {

    private String plainNo1;
    private String plainNo2;
    private String stopFlag;
    private int sectOrd;

    public String getPlainNo1() {
        return plainNo1;
    }

    public void setPlainNo1(String plainNo1) {
        this.plainNo1 = plainNo1;
    }

    public String getPlainNo2() {
        return plainNo2;
    }

    public void setPlainNo2(String plainNo2) {
        this.plainNo2 = plainNo2;
    }

    public String getStopFlag() {
        return stopFlag;
    }

    public void setStopFlag(String stopFlag) {
        this.stopFlag = stopFlag;
    }

    public int getSectOrd() {
        return sectOrd;
    }

    public void setSectOrd(int sectOrd) {
        this.sectOrd = sectOrd;
    }
}
